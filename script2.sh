uname -a # uname viene de 'unix name' y muestra la informacion basica del sistema -a significa all 
lscpu # significa 'list cpu' muestra la info de la cpu
free -h # free muestra el estado de la ram y del swap y -h hace que sea legible para humanos 
df -h # df significa 'disk free' y muestra la info de los discos duros o particiones y lo hace legible para humanos con -h